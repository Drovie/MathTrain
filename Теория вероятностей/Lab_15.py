P_A_H3 = 6/12

"По формуле полной вероятности получаем:"
P_A = P_H1 * P_A_H1 + P_H2 * P_A_H2 + P_H3 * P_A_H3
print("Вероятность, что из левого кармана достант монету в 10 коп. равна: ", P_A)