"Для подсчёта вероятностей необходимо посчитать количество благоприятных и всех исходов, для этого посчитаем сочетания"
"В каждом кармане по 7 монет, а выбирают 5. Тогда общее число исходов:"
C_H = math.comb(7, 5)

#P_H1
C_1_10к = math.comb(3, 1)
C_4_5к = math.comb(4, 4)

P_H1 = (C_1_10к * C_4_5к)/C_H
print("Вероятность H1: ", P_H1)


#P_H2
C_2_10к = math.comb(3, 2)
C_3_5к = math.comb(4, 3)

P_H2 = (C_2_10к * C_3_5к)/C_H
print("Вероятность H2: ", P_H2)


#P_H3
C_3_10к = math.comb(3, 3)
C_2_5к = math.comb(4, 2)

P_H3 = (C_3_10к * C_2_5к)/C_H
print("Вероятность H3: ", P_H3)


"Условные вероятности"
"После перекладывания в левом кармане стало 12 монет, тогда:"
P_A_H1 = 4/12
